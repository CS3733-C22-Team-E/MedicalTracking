package edu.wpi.teame.view.map.Astar.MapIntegration;

public class HeightWidth {
  double Width;
  double Height;

  public HeightWidth(double width, double height) {
    this.Width = width;
    this.Height = height;
  }

  public double getWidth() {
    return Width;
  }

  public void setWidth(double width) {
    Width = width;
  }

  public double getHeight() {
    return Height;
  }

  public void setHeight(double height) {
    Height = height;
  }
}
